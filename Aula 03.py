# Laços de Repetição + Listas
'''
for item in coleção:
 expressão
 range vai até o penultimo numero (1,21)
 range (1,20,2) vai pular de dois em dois
'''

for palavra in range (1,4):
    print('carregando')

for item in range(1,21):
    print(item)

for item in range(1,20,2):
    print(item)

nomes = ['babi', 'julia', 'rosa']
for nome in nomes:
    print(nome)
